# ---------------------------------------------------------------------------
# 便捷查询
# ---------------------------------------------------------------------------
def get_profile_prompt(profile_id: str | None) -> str:
    if not profile_id:
        return ""
    for p in INVESTOR_PROFILES:
        if p["id"] == profile_id:
            return p["prompt"]
    return ""


def get_report_style_prompt(style_id: str | None) -> str:
    if not style_id:
        return ""
    for s in REPORT_STYLES:
        if s["id"] == style_id:
            return s["prompt"]
    return ""


def list_profiles_public() -> list[dict]:
    """暴露给前端的字段（不含完整 prompt，减少带宽）。"""
    return [
        {"id": p["id"], "name": p["name"], "tagline": p["tagline"]}
        for p in INVESTOR_PROFILES
    ]


def list_report_styles_public() -> list[dict]:
    return [
        {"id": s["id"], "name": s["name"], "tagline": s["tagline"]}
        for s in REPORT_STYLES
    ]
